#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	n;
	char	*ptr;

	n = ft_strlen(s1);
	ptr = malloc((n + 1) * sizeof(char));
	if (!ptr)
		return (NULL);
	return (ft_memmove(ptr, s1, n + 1));
}
